        <p>Rotaract brings together adults ages 18-30 to take action in their communities, develop their leadership and professional skills, and have fun.  Its membership totals over 184,000 in more than 8,000 clubs worldwide. Rotaract clubs are self-governing and self-supporting and can be either university- or community-based. Individual Rotary clubs sponsor Rotaract clubs and offer guidance and support, making the Rotaract clubs true &quot;partners in service&quot; and key members of the family of Rotary. &quot;Rotaract&quot; stands for &quot;Rotary in Action&quot;, although the name originally comes from a combination of &quot;Rotary&quot; and &quot;Interact&quot; (International + Action), the high school level program created by Rotary International in 1962.<p>
